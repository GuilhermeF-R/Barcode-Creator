#O método __init__ é um método especial em Python que é chamado automaticamente
# quando uma nova instância de uma classe é criada.
# Ele é conhecido como o construtor da classe e é utilizado para inicializar os
# atributos da instância.
#A principal finalidade do método __init__ é garantir que as
# instâncias de uma classe estejam em um estado inicial válido, definindo
# valores padrão para seus atributos ou executando outras operações de inicialização necessárias.
#Por exemplo, se você tem uma classe Carro com atributos como modelo, ano e cor,
#você pode usar o método __init__ para definir os valores padrão desses
# atributos quando um novo carro é criado.
